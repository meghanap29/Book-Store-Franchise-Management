-- Delete from F23_S001_T1_SOLD_BY and F23_S001_T1_BUYS
DELETE FROM F23_S001_T1_SOLD_BY WHERE ROWNUM <= 300;
DELETE FROM F23_S001_T1_BUYS WHERE ROWNUM <= 1000;


-- Delete some records from F23_S001_T1_PURCHASES_FROM
DELETE FROM F23_S001_T1_PURCHASES_FROM
WHERE Amount < 30;


-- Update F23_S001_T1_BOOK_GENRE
UPDATE F23_S001_T1_BOOK_GENRE
SET Genre = 'Historical'
WHERE ISBN IN (
    SELECT ISBN
    FROM F23_S001_T1_BOOK_GENRE
    WHERE Genre = 'Romance'
);


-- Insert records in F23_S001_T1_SOLD_BY
INSERT INTO F23_S001_T1_SOLD_BY (ISBN, Store_ID)
SELECT ISBN, INVENTORYID
FROM F23_S001_T1_STOCKED_IN
WHERE ROWNUM <= 20;

-- Update some rows in F23_S001_T1_PURCHASES_FROM
UPDATE F23_S001_T1_PURCHASES_FROM
SET Amount = Amount * 5
WHERE StoreID IN (
    SELECT Store_ID
    FROM F23_S001_T1_STORE
    WHERE ROWNUM <= 300
);